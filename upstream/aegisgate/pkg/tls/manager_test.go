func TestCertificateValidity(t *testing.T) {
	tempDir := t.TempDir()

	cfg := &Config{
		CertDir:      tempDir,
		AutoGenerate: true,
	}

	mgr, err := NewManager(cfg)
	if err != nil {
		t.Fatalf("NewManager() error = %v", err)
	}

	info, err := mgr.GetCertificateInfo()
	if err != nil {
		t.Fatalf("GetCertificateInfo() error = %v", err)
	}

	// Parse dates
	notBefore, ok := info["not_before"].(string)
	if !ok {
		t.Fatal("not_before is not a string")
	}

	notAfter, ok := info["not_after"].(string)
	if !ok {
		t.Fatal("not_after is not a string")
	}

	before, err := time.Parse("2006-01-02 15:04:05", notBefore)
	if err != nil {
		t.Fatalf("Failed to parse not_before: %v", err)
	}

	after, err := time.Parse("2006-01-02 15:04:05", notAfter)
	if err != nil {
		t.Fatalf("Failed to parse not_after: %v", err)
	}

	now := time.Now()

	// Certificate should be valid now
	if now.Before(before) {
		t.Error("Certificate is not yet valid")
	}

	if now.After(after) {
		t.Error("Certificate has expired")
	}

	// Should be valid for approximately 1 year
	duration := after.Sub(before)
	expectedDuration := 365 * 24 * time.Hour
	if duration < expectedDuration-24*time.Hour || duration > expectedDuration+24*time.Hour {
		t.Errorf("Certificate validity period = %v, expected ~1 year", duration)
	}
}

