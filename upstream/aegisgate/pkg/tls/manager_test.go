func TestNewManager(t *testing.T) {
	tempDir := t.TempDir()

	cfg := &Config{
		CertDir:      tempDir,
		AutoGenerate: true,
	}

	mgr, err := NewManager(cfg)
	if err != nil {
		t.Fatalf("NewManager() error = %v", err)
	}

	if mgr == nil {
		t.Fatal("NewManager() returned nil")
	}

	if mgr.certDir != tempDir {
		t.Errorf("certDir = %s, want %s", mgr.certDir, tempDir)
	}

	// Check certificates were generated
	certPath := filepath.Join(tempDir, "server.crt")
	keyPath := filepath.Join(tempDir, "server.key")

	if _, err := os.Stat(certPath); os.IsNotExist(err) {
		t.Error("Certificate file was not generated")
	}

	if _, err := os.Stat(keyPath); os.IsNotExist(err) {
		t.Error("Key file was not generated")
	}
}

func TestNewManagerWithExistingCerts(t *testing.T) {
	tempDir := t.TempDir()

	// Create first manager to generate certs
	cfg1 := &Config{
		CertDir:      tempDir,
		AutoGenerate: true,
	}

	mgr1, err := NewManager(cfg1)
	if err != nil {
		t.Fatalf("First NewManager() error = %v", err)
	}

	certPath, keyPath := mgr1.GetCertificatePaths()

	// Create second manager - should reuse existing certs
	cfg2 := &Config{
		CertDir:      tempDir,
		AutoGenerate: true,
	}

	mgr2, err := NewManager(cfg2)
	if err != nil {
		t.Fatalf("Second NewManager() error = %v", err)
	}

	certPath2, keyPath2 := mgr2.GetCertificatePaths()

	if certPath != certPath2 {
		t.Errorf("Certificate paths differ: %s vs %s", certPath, certPath2)
	}

	if keyPath != keyPath2 {
		t.Errorf("Key paths differ: %s vs %s", keyPath, keyPath2)
	}
}

func TestGetCertificateInfo(t *testing.T) {
	tempDir := t.TempDir()

	cfg := &Config{
		CertDir:      tempDir,
		AutoGenerate: true,
	}

	mgr, err := NewManager(cfg)
	if err != nil {
		t.Fatalf("NewManager() error = %v", err)
	}

	info, err := mgr.GetCertificateInfo()
	if err != nil {
		t.Fatalf("GetCertificateInfo() error = %v", err)
	}

	// Check required fields
	requiredFields := []string{"subject", "issuer", "not_before", "not_after", "auto_generated"}
	for _, field := range requiredFields {
		if _, ok := info[field]; !ok {
			t.Errorf("Certificate info missing field: %s", field)
		}
	}

	// Verify auto_generated is true
	if autoGen, ok := info["auto_generated"].(bool); !ok || !autoGen {
		t.Error("Expected auto_generated to be true")
	}
}

